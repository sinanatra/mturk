    <p>
        The images that have been described are a result of the same process. On
        June 16, 2022, I started this experiment by asking the workers at Amazon
        Mechanical Turk to send me a photo of their view out of the window and
        the city they live in for $0.20.
    </p>

    <p>
        As organisations use Mechanical Turk to create datasets, in this
        experiment a more private dataset has been created. Following the logic
        of the platform, for a small fee workers have been asked to show a
        glimpse of their private life, their view close to their desk, their
        working station. At the limit of ethics and labour exploitation I asked
        myself whether this is an acceptable approach to use in research and in
        general, if employing random&nbsp;workers around the world is the ideal
        practice to shape future artificial intelligences.
    </p>
    <p>
        While both tasks were performed, the material collected varied
        considerably: from blurred photos to details of curtains, from
        landscapes to computer screenshots.
    </p>
    <p>
        The same consideration can be made with the texts collected. Some
        descriptions are lengthy, others are short, some are tags, others
        instead are just typos. Following the idea that when talking about a
        technology one has to place oneself within it, becoming a 'Requester' on
        Amazon Mechanical Turk allowed me to find out more about how the system
        works, the review process and the quality of the data collected.
    </p>
    <p>
        Although much is documented about the demographics of the platform and
        the fact that different payments do not affect the quality of the
        results, these 'results' are rarely visible. Most of the data collected
        did not reflect the assigned task. Many results were gibberish, or when
        asked to upload a photo, these were often desktop screenshots, stock
        images, or photos on the computer. These rebellious approaches to the
        platform allow workers to collect their rewards easily and can be
        ignored by requesters. For this reason, such datasets are shown in their
        entirety on this site.
    </p>

    <quote
        >“Information has always had geography. It is from somewhere; about
        somewhere; it evolves and is transformed somewhere; it is mediaxted by
        networks, infrastructures, and technologies: all of which exist in
        physical, material places”. <a
            href="https://rgs-ibg.onlinelibrary.wiley.com/doi/full/10.1002/geo2.8"
            target="_blank">See link</a
        >.
    </quote>
    <p>
        Datasets are no exception. The information they carry is a reflection of
        time, politics and geographical contexts.
        <br />
        Although, in the past years, there has been a growing attention of bias studies
        in the field of machine learning, there is still a general disinterest for
        the ones carried by the hidden geographies of datasets. Therefore there is
        a central focus on the geographies of Amazon Mechanical Turk. Not necessarly
        on the demographics of its workers, but rather on the different places where
        data is annotated and created.
        <br />
    </p>
