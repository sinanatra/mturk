    <h1>Who is working for Amazon Mechanical Turk?</h1>
    <h2>
        These experiments delve into the lives of Amazon Mechanical Turk
        workers. Following the logic of the platform, workers have been employed
        for a small fee and asked to give a glimpse of their private lifes.
        Unlike the typical use of the platform, the submitted data prompt
        reflection on hidden labor, material quality, and ways of rebelling
        against a system on the verge of ethics.
    </h2>
    <figure>
        <img src="./mturk.png" alt="Mechanical Turk" />
        <figcaption>
            Drawing of von Kempelen's Mechanical Turk (Villatoro 2013).
        </figcaption>
    </figure>
    <p>
        The ever-growing use of platforms such as Amazon Mechanical Turk, allow
        large groups of individuals to participate in creating, tagging and
        describing data.
        <br />
        Introduced by Amazon in 2005, Mechanical Turk is named after an 18th century
        “automatic” chess-playing machine, which was handily beating humans in chess
        games. Of course, the robot was not using any artificial intelligence algorithms
        back then. The secret of the Mechanical Turk machine was a human operator,
        hidden inside the machine,who was the real intelligence.
        <a href="https://doi.org/10.1145/1869086.1869094" target="_blank"
            >See link</a
        >.
        <br /><br />

        Today, Amazon Mechanical Turk is a marketplace for tasks that cannot be
        easily automated, which are then fulfilled by human users. Such tasks,
        mainly involve the creation of datasets: collections of data that act as
        pedagogical learning material when dealing with Artificial
        intelligences, most specifically, machine learning systems. Current
        Artificial intelligences are able to "learn” to execute such tasks by
        simply repeatedly analysing large numbers of labelled examples and
        counterexamples.
        <br />
        Such data is analysed and processed by workers on platforms like Mechanical
        Turks. But little is known about these individuals that complete these tasks.
    </p>
